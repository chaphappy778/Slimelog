import { createServerClient } from "@supabase/ssr";
import { cookies } from "next/headers";
import { redirect } from "next/navigation";
import Link from "next/link";
import PageHeader from "@/components/PageHeader";
import PageWrapper from "@/components/PageWrapper";
import FloatingPills from "@/components/FloatingPills";
import ProfileFeaturedSection from "@/components/profile/ProfileFeaturedSection";
// [Change: replaced local TYPE_LABEL constant with SLIME_TYPE_LABELS from @/lib/types]
import { SLIME_TYPE_LABELS, type SlimeType } from "@/lib/types";

// ─── Types ─────────────────────────────────────────────────────────────────────

type CollectionSummary = {
  user_id: string;
  total_in_collection: number | null;
  total_in_wishlist: number | null;
  total_rated: number | null;
  avg_overall_given: number | null;
  distinct_brands_tried: number | null;
  distinct_types_tried: number | null;
};

type RecentLog = {
  id: string;
  created_at: string;
  slime_name: string | null;
  brand_name_raw: string | null;
  slime_type: string | null;
  rating_overall: number | null;
  brands: { name: string | null }[] | null;
};

// [Change: types for featured slimes data passed to ProfileFeaturedSection]
type FeaturedLog = {
  id: string;
  slime_name: string | null;
  brand_name_raw: string | null;
  slime_type: string | null;
  rating_overall: number | null;
  image_url: string | null;
  colors: string[] | null;
};

type CandidateLog = {
  id: string;
  slime_name: string | null;
  brand_name_raw: string | null;
  slime_type: string | null;
  rating_overall: number | null;
  image_url: string | null;
  colors: string[] | null;
};

// ─── Helpers ───────────────────────────────────────────────────────────────────

function resolveDisplayName(username: string): {
  displayName: string;
  handle: string | null;
  isAutoGenerated: boolean;
} {
  if (username.startsWith("user_"))
    return { displayName: "Slimer", handle: username, isAutoGenerated: true };
  return { displayName: username, handle: null, isAutoGenerated: false };
}

// ─── Sub-components ────────────────────────────────────────────────────────────

// [Fix 38] StatTile gains a `variant` prop:
//   - "number" (default): existing gradient-text numeral look
//   - "pill":             value renders as a small magenta pill, matching the public profile's
//                         "Fav type" treatment. Null/undefined values fall back to an em-dash.
// All existing call sites that omit `variant` continue to work unchanged.
// [Follow-up] Added min-h-20 to the outer container so the pill variant (which
// is intrinsically shorter than the gradient-text number variant) doesn't make
// the 2x2 stats grid look ragged. All four tiles now share a consistent height.
function StatTile({
  value,
  label,
  accent,
  variant = "number",
}: {
  value: string | number | null;
  label: string;
  accent: string;
  variant?: "number" | "pill";
}) {
  return (
    <div
      className="rounded-2xl p-4 flex flex-col gap-1 min-h-20"
      style={{
        background: "rgba(45,10,78,0.3)",
        border: "1px solid rgba(45,10,78,0.7)",
        boxShadow: "inset 0 0 16px rgba(45,10,78,0.2)",
      }}
    >
      {variant === "pill" ? (
        value != null && value !== "" ? (
          // [Fix 38] Magenta pill treatment — matches public profile Fav type pill.
          <span
            className="text-xs font-bold px-2 py-0.5 rounded-full self-start"
            style={{
              background: "rgba(255,0,229,0.12)",
              color: "#FF00E5",
              border: "1px solid rgba(255,0,229,0.3)",
            }}
          >
            {value}
          </span>
        ) : (
          // [Fix 38] Empty fallback for pill variant — small muted em-dash, not an empty pill.
          <span className="text-sm text-slime-muted leading-none">—</span>
        )
      ) : (
        <p
          className="text-2xl font-black tabular-nums leading-none"
          style={{
            background: accent,
            WebkitBackgroundClip: "text",
            WebkitTextFillColor: "transparent",
          }}
        >
          {value ?? "—"}
        </p>
      )}
      <p className="text-xs text-slime-muted font-medium leading-snug">
        {label}
      </p>
    </div>
  );
}

function RecentLogRow({ log }: { log: RecentLog }) {
  const brandName =
    log.brands?.[0]?.name ?? log.brand_name_raw ?? "Unknown brand";
  // [Change: use SLIME_TYPE_LABELS instead of local TYPE_LABEL]
  const typeLabel =
    (log.slime_type && SLIME_TYPE_LABELS[log.slime_type as SlimeType]) ??
    log.slime_type ??
    "—";
  // [Change: replaced date-fns formatDistanceToNow with inline Intl.DateTimeFormat per absolute rules]
  const timeAgo = new Intl.DateTimeFormat("en-US", {
    month: "short",
    day: "numeric",
  }).format(new Date(log.created_at));

  return (
    <li className="flex items-center gap-3 py-3 border-b border-slime-border/50 last:border-0">
      <div
        className="w-9 h-9 rounded-2xl shrink-0 flex items-center justify-center bg-slime-surface border border-slime-border"
        aria-hidden="true"
      >
        <svg width="18" height="18" viewBox="0 0 24 24" fill="none">
          <circle cx="12" cy="12" r="9" stroke="#00F0FF" strokeWidth="1.5" />
          <circle cx="9" cy="9" r="2" stroke="#00F0FF" strokeWidth="1" />
          <circle cx="14" cy="8" r="1" stroke="#00F0FF" strokeWidth="1" />
        </svg>
      </div>
      <div className="flex-1 min-w-0">
        <p className="text-sm font-semibold text-slime-text truncate leading-tight">
          {log.slime_name ?? "Untitled slime"}
        </p>
        <p className="text-xs text-slime-muted truncate">
          {brandName} · <span className="text-slime-magenta">{typeLabel}</span>
        </p>
      </div>
      <div className="text-right shrink-0">
        {log.rating_overall ? (
          <p className="text-sm font-bold text-slime-cyan">
            {log.rating_overall}/5
          </p>
        ) : (
          <p className="text-xs text-slime-muted">no rating</p>
        )}
        <time
          className="text-[10px] text-slime-muted"
          dateTime={log.created_at}
        >
          {timeAgo}
        </time>
      </div>
    </li>
  );
}

// ─── Page ──────────────────────────────────────────────────────────────────────

export default async function ProfilePage() {
  const cookieStore = await cookies();
  const supabase = createServerClient(
    process.env.NEXT_PUBLIC_SUPABASE_URL!,
    process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY!,
    { cookies: { get: (name) => cookieStore.get(name)?.value } },
  );

  const {
    data: { user },
  } = await supabase.auth.getUser();
  if (!user) redirect("/login");

  // [Fix 39] Extended profiles select to include instagram_handle, tiktok_handle, shop_url
  // so the owner profile can render the same social links row the public profile shows.
  const [summaryResult, profileResult, recentLogsResult, allLogsResult] =
    await Promise.all([
      supabase
        .from("user_collection_summary")
        .select(
          "user_id, total_in_collection, total_in_wishlist, total_rated, avg_overall_given, distinct_brands_tried, distinct_types_tried",
        )
        .eq("user_id", user.id)
        .maybeSingle(),
      supabase
        .from("profiles")
        .select(
          "username, avatar_url, featured_log_ids, instagram_handle, tiktok_handle, shop_url",
        )
        .eq("id", user.id)
        .maybeSingle(),
      supabase
        .from("collection_logs")
        .select(
          "id, created_at, slime_name, brand_name_raw, slime_type, rating_overall, brands ( name )",
        )
        .eq("user_id", user.id)
        .order("created_at", { ascending: false })
        .limit(5),
      // [Change: fetch all in_collection logs as candidates for the editor]
      supabase
        .from("collection_logs")
        .select(
          "id, slime_name, brand_name_raw, slime_type, rating_overall, image_url, colors",
        )
        .eq("user_id", user.id)
        .eq("in_collection", true)
        .order("created_at", { ascending: false }),
    ]);

  const summary: CollectionSummary | null = summaryResult.data ?? null;
  const rawUsername = profileResult.data?.username ?? "user_unknown";
  const avatarUrl = profileResult.data?.avatar_url ?? null;
  // [Fix 39] Destructure social handles from the profile row
  const instagramHandle = profileResult.data?.instagram_handle ?? null;
  const tiktokHandle = profileResult.data?.tiktok_handle ?? null;
  const shopUrl = profileResult.data?.shop_url ?? null;
  const { displayName, handle, isAutoGenerated } =
    resolveDisplayName(rawUsername);
  const recentLogs: RecentLog[] = recentLogsResult.data ?? [];
  const allCollectionLogs: CandidateLog[] = (allLogsResult.data ??
    []) as CandidateLog[];

  // [Fix 39] Conditional render flag for the social links row
  const hasSocialLinks = !!instagramHandle || !!tiktokHandle || !!shopUrl;

  // [Change: resolve featured_log_ids and fetch the actual log rows in order]
  const rawFeaturedIds: string[] = profileResult.data?.featured_log_ids ?? [];
  const featuredIds = rawFeaturedIds.filter((id): id is string => !!id);
  let featuredLogs: FeaturedLog[] = [];

  if (featuredIds.length > 0) {
    const { data: featuredData } = await supabase
      .from("collection_logs")
      .select(
        "id, slime_name, brand_name_raw, slime_type, rating_overall, image_url, colors",
      )
      .in("id", featuredIds);

    // Preserve the order of featuredIds
    const byId: Record<string, FeaturedLog> = {};
    for (const row of featuredData ?? []) {
      byId[row.id] = row as FeaturedLog;
    }
    featuredLogs = featuredIds.map((id) => byId[id]).filter(Boolean);
  }

  // [Change: use SLIME_TYPE_LABELS for fave type computation]
  const typeCounts: Record<string, number> = {};
  for (const log of recentLogs) {
    if (log.slime_type)
      typeCounts[log.slime_type] = (typeCounts[log.slime_type] ?? 0) + 1;
  }
  const topType =
    Object.entries(typeCounts).sort((a, b) => b[1] - a[1])[0]?.[0] ?? null;
  const topTypeLabel = topType
    ? (SLIME_TYPE_LABELS[topType as SlimeType] ?? topType)
    : null;

  const hasErrors =
    summaryResult.error ||
    profileResult.error ||
    recentLogsResult.error ||
    allLogsResult.error;

  return (
    <PageWrapper dots glow="cyan">
      <PageHeader />

      <div className="pt-14">
        {/* Profile hero with floating pills */}
        <div className="relative px-4 pt-8 pb-6 overflow-hidden">
          <FloatingPills area="section" density="low" zIndex={0} />

          <div className="relative z-10 flex items-center gap-4">
            {/* Holographic avatar ring */}
            <div
              className="p-0.5 rounded-3xl shrink-0"
              style={{
                background:
                  "linear-gradient(135deg, #39FF14, #00F0FF, #FF00E5)",
              }}
            >
              <div className="w-16 h-16 rounded-3xl overflow-hidden bg-slime-bg flex items-center justify-center">
                {avatarUrl ? (
                  // eslint-disable-next-line @next/next/no-img-element
                  <img
                    src={avatarUrl}
                    alt="Avatar"
                    className="w-full h-full object-cover"
                  />
                ) : (
                  <div
                    className="w-full h-full flex items-center justify-center text-slime-bg text-2xl font-black"
                    style={{
                      background: "linear-gradient(135deg, #39FF14, #00F0FF)",
                    }}
                    aria-hidden="true"
                  >
                    {displayName.charAt(0).toUpperCase()}
                  </div>
                )}
              </div>
            </div>

            <div className="flex-1 min-w-0">
              <h1 className="text-xl font-black text-slime-cyan leading-tight truncate">
                {displayName}
              </h1>
              <p className="text-sm text-slime-magenta mt-0.5 truncate">
                @{handle ?? rawUsername}
              </p>
              {isAutoGenerated ? (
                <Link
                  href="/settings/profile"
                  className="inline-flex items-center gap-1 text-xs text-slime-accent mt-1 font-medium active:opacity-70 transition-opacity"
                >
                  {/* Inline pencil SVG — no lucide import */}
                  <svg
                    width="14"
                    height="14"
                    viewBox="0 0 24 24"
                    fill="none"
                    stroke="currentColor"
                    strokeWidth="2"
                    strokeLinecap="round"
                    strokeLinejoin="round"
                  >
                    <path d="M11 4H4a2 2 0 0 0-2 2v14a2 2 0 0 0 2 2h14a2 2 0 0 0 2-2v-7" />
                    <path d="M18.5 2.5a2.121 2.121 0 0 1 3 3L12 15l-4 1 1-4 9.5-9.5z" />
                  </svg>
                  Tap to set a custom username
                </Link>
              ) : (
                <Link
                  href="/settings/profile"
                  className="inline-flex items-center gap-1 text-xs text-slime-muted mt-1 font-medium active:opacity-70 transition-opacity hover:text-slime-accent"
                >
                  <svg
                    width="14"
                    height="14"
                    viewBox="0 0 24 24"
                    fill="none"
                    stroke="currentColor"
                    strokeWidth="2"
                    strokeLinecap="round"
                    strokeLinejoin="round"
                  >
                    <path d="M11 4H4a2 2 0 0 0-2 2v14a2 2 0 0 0 2 2h14a2 2 0 0 0 2-2v-7" />
                    <path d="M18.5 2.5a2.121 2.121 0 0 1 3 3L12 15l-4 1 1-4 9.5-9.5z" />
                  </svg>
                  Edit profile
                </Link>
              )}
            </div>
          </div>

          {/* [Fix 39] Social links row — icon-only 36x36 circular pill buttons.
              Visual treatment copied verbatim from apps/web/app/users/[username]/page.tsx
              (rgba(45,10,78,0.25) bg, rgba(45,10,78,0.7) border, cyan #00F0FF icon,
              hover → slime-accent). SVGs bumped to 18x18 to match Fix 37.
              Row sits below the main avatar/name/edit block but still inside the hero
              container so it reads as part of the profile header. Renders nothing
              when no social links are set. */}
          {hasSocialLinks && (
            <div className="relative z-10 flex items-center gap-2 mt-3">
              {instagramHandle && (
                <a
                  href={`https://instagram.com/${instagramHandle}`}
                  target="_blank"
                  rel="noopener noreferrer"
                  aria-label={`Instagram profile: @${instagramHandle}`}
                  className="flex items-center justify-center w-9 h-9 rounded-full transition-colors hover:text-slime-accent"
                  style={{
                    background: "rgba(45,10,78,0.25)",
                    border: "1px solid rgba(45,10,78,0.7)",
                    color: "#00F0FF",
                  }}
                >
                  <svg
                    width="18"
                    height="18"
                    viewBox="0 0 24 24"
                    fill="none"
                    stroke="currentColor"
                    strokeWidth="2"
                    strokeLinecap="round"
                    strokeLinejoin="round"
                    aria-hidden="true"
                  >
                    <rect x="2" y="2" width="20" height="20" rx="5" ry="5" />
                    <path d="M16 11.37A4 4 0 1 1 12.63 8 4 4 0 0 1 16 11.37z" />
                    <line x1="17.5" y1="6.5" x2="17.51" y2="6.5" />
                  </svg>
                </a>
              )}
              {tiktokHandle && (
                <a
                  href={`https://www.tiktok.com/@${tiktokHandle}`}
                  target="_blank"
                  rel="noopener noreferrer"
                  aria-label={`TikTok profile: @${tiktokHandle}`}
                  className="flex items-center justify-center w-9 h-9 rounded-full transition-colors hover:text-slime-accent"
                  style={{
                    background: "rgba(45,10,78,0.25)",
                    border: "1px solid rgba(45,10,78,0.7)",
                    color: "#00F0FF",
                  }}
                >
                  <svg
                    width="18"
                    height="18"
                    viewBox="0 0 24 24"
                    fill="none"
                    stroke="currentColor"
                    strokeWidth="2"
                    strokeLinecap="round"
                    strokeLinejoin="round"
                    aria-hidden="true"
                  >
                    <path d="M9 18V5l12-2v13" />
                    <circle cx="6" cy="18" r="3" />
                    <circle cx="18" cy="16" r="3" />
                  </svg>
                </a>
              )}
              {shopUrl && (
                <a
                  href={shopUrl}
                  target="_blank"
                  rel="noopener noreferrer"
                  aria-label={`Shop: ${shopUrl}`}
                  className="flex items-center justify-center w-9 h-9 rounded-full transition-colors hover:text-slime-accent"
                  style={{
                    background: "rgba(45,10,78,0.25)",
                    border: "1px solid rgba(45,10,78,0.7)",
                    color: "#00F0FF",
                  }}
                >
                  <svg
                    width="18"
                    height="18"
                    viewBox="0 0 24 24"
                    fill="none"
                    stroke="currentColor"
                    strokeWidth="2"
                    strokeLinecap="round"
                    strokeLinejoin="round"
                    aria-hidden="true"
                  >
                    <path d="M6 2L3 6v14a2 2 0 0 0 2 2h14a2 2 0 0 0 2-2V6l-3-4z" />
                    <line x1="3" y1="6" x2="21" y2="6" />
                    <path d="M16 10a4 4 0 0 1-8 0" />
                  </svg>
                </a>
              )}
            </div>
          )}
        </div>

        {hasErrors && (
          <div className="mx-4 mb-4 px-4 py-3 rounded-2xl bg-red-500/10 border border-red-500/30 text-xs text-red-400">
            Some stats couldn&apos;t load — try refreshing.
          </div>
        )}

        {/* [Change: Featured Slimes section — client component, above stats, omitted if empty] */}
        <ProfileFeaturedSection
          initialFeaturedLogs={featuredLogs}
          initialFeaturedIds={featuredIds}
          allCollectionLogs={allCollectionLogs}
          userId={user.id}
        />

        {/* Stats */}
        <section className="px-4 mb-8">
          <p className="section-label mb-3">Collection Stats</p>
          <div className="grid grid-cols-2 gap-3">
            <StatTile
              value={summary?.total_in_collection ?? 0}
              label="Slimes logged"
              accent="#39FF14"
            />
            <StatTile
              value={
                summary?.avg_overall_given != null
                  ? summary.avg_overall_given.toFixed(1)
                  : null
              }
              label="Avg rating given"
              accent="#00F0FF"
            />
            <StatTile
              value={summary?.distinct_brands_tried ?? 0}
              label="Brands tried"
              accent="#FF00E5"
            />
            {/* [Fix 38] Fav type tile switches to pill variant so slime-type labels
                render as small magenta pills — matching the public profile treatment —
                instead of oversized gradient text. Other tiles keep variant="number"
                (default). */}
            <StatTile
              value={topTypeLabel ?? summary?.distinct_types_tried ?? 0}
              label={topTypeLabel ? "Fave type (recent)" : "Types tried"}
              accent="#39FF14"
              variant={topTypeLabel ? "pill" : "number"}
            />
          </div>

          {summary != null && (
            <div className="mt-3">
              <Link
                href="/wishlist"
                className="inline-flex items-center gap-1.5 text-sm font-semibold transition-opacity active:opacity-70"
                style={{ color: "#CC44FF", textDecoration: "none" }}
              >
                <svg
                  width="14"
                  height="14"
                  viewBox="0 0 24 24"
                  fill="none"
                  stroke="currentColor"
                  strokeWidth="2"
                  strokeLinecap="round"
                  strokeLinejoin="round"
                >
                  <path d="M20.84 4.61a5.5 5.5 0 0 0-7.78 0L12 5.67l-1.06-1.06a5.5 5.5 0 0 0-7.78 7.78l1.06 1.06L12 21.23l7.78-7.78 1.06-1.06a5.5 5.5 0 0 0 0-7.78z" />
                </svg>
                {summary.total_in_wishlist ?? 0} wishlisted
              </Link>
            </div>
          )}
        </section>

        {/* Recent activity */}
        <section className="px-4 pb-24">
          <p className="section-label mb-3">Recent Activity</p>
          <div
            className="rounded-2xl px-4"
            style={{
              background: "rgba(45,10,78,0.2)",
              border: "1px solid rgba(45,10,78,0.6)",
            }}
          >
            {recentLogs.length === 0 ? (
              <div className="py-10 text-center text-slime-muted text-sm">
                No logs yet — tap{" "}
                <span className="font-semibold text-slime-accent">+ Log</span>{" "}
                to add your first slime.
              </div>
            ) : (
              <ul role="list">
                {recentLogs.map((log) => (
                  <RecentLogRow key={log.id} log={log} />
                ))}
              </ul>
            )}
          </div>
        </section>
      </div>
    </PageWrapper>
  );
}
