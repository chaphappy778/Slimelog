function Stars({ rating }: { rating: number | null }) {
  if (!rating) return <span className="text-xs text-slime-muted">—</span>;
  return (
    <span
      className="flex items-center gap-0.5"
      aria-label={`${rating} out of 5`}
    >
      {[1, 2, 3, 4, 5].map((n) => (
        <svg
          key={n}
          width="12"
          height="12"
          viewBox="0 0 24 24"
          fill={n <= rating ? "#39FF14" : "rgba(57,255,20,0.15)"}
          aria-hidden="true"
        >
          <polygon points="12,2 15.09,8.26 22,9.27 17,14.14 18.18,21.02 12,17.77 5.82,21.02 7,14.14 2,9.27 8.91,8.26" />
        </svg>
      ))}
    </span>
  );
}

// [Fix A] StatTile rewritten for horizontal-scroll layout:
//   - Outer container adds `snap-start shrink-0 w-36` so tiles behave as fixed-width
//     snap targets inside the scroll container.
//   - Number variant now uses a solid color via `style.color`, not gradient-text.
//     The `accent` prop takes a plain hex string (e.g. "#39FF14") rather than a
//     gradient definition.
//   - Pill variant scales up to text-base / px-3 py-1 / font-black / leading-tight
//     so the "Fav type" tile no longer looks underweight next to the 2xl numerals.
//   - Label styling and empty-state em-dash for pill variant are unchanged.
// Function signature is preserved.
function StatTile({
  value,
  label,
  accent,
  variant = "number",
}: {
  value: string | number | null;
  label: string;
  accent: string;
  variant?: "number" | "pill";
}) {
  return (
    <div
      className="snap-start shrink-0 w-36 rounded-2xl p-4 flex flex-col gap-1 min-h-20"
      style={{
        background: "rgba(45,10,78,0.3)",
        border: "1px solid rgba(45,10,78,0.7)",
        boxShadow: "inset 0 0 16px rgba(45,10,78,0.2)",
      }}
    >
      {variant === "pill" ? (
        value != null && value !== "" ? (
          // [Fix A] Magenta pill — scaled up from text-xs / px-2 py-0.5 to
          // text-base / px-3 py-1 / font-black / leading-tight.
          <span
            className="text-base font-black px-3 py-1 rounded-full self-start leading-tight"
            style={{
              background: "rgba(255,0,229,0.12)",
              color: "#FF00E5",
              border: "1px solid rgba(255,0,229,0.3)",
            }}
          >
            {value}
          </span>
        ) : (
          <span className="text-sm text-slime-muted leading-none">—</span>
        )
      ) : (
        // [Fix A] Solid-color numeral — replaces prior gradient background-clip.
        // `accent` is a flat hex color string like "#39FF14", "#00F0FF", "#FF00E5".
        <p
          className="text-2xl font-black tabular-nums leading-none"
          style={{ color: accent }}
        >
          {value ?? "—"}
        </p>
      )}
      <p className="text-xs text-slime-muted font-medium leading-snug">
        {label}
      </p>
    </div>
  );
}

