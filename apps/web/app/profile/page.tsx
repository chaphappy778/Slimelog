// ─── Helpers ───────────────────────────────────────────────────────────────────

function resolveDisplayName(username: string): {
  displayName: string;
  handle: string | null;
  isAutoGenerated: boolean;
} {
  if (username.startsWith("user_"))
    return { displayName: "Slimer", handle: username, isAutoGenerated: true };
  return { displayName: username, handle: null, isAutoGenerated: false };
}

// [Fix B] Inline relative time — copied verbatim from apps/web/app/users/[username]/page.tsx
// so the owner profile's Recent logs cards show the same "Xh ago" / "Xd ago" format as the
// public profile. Absolute rule compliance: no date-fns, inline Intl only.
function formatRelativeTime(isoString: string): string {
  const diffMs = Date.now() - new Date(isoString).getTime();
  const diffMins = Math.floor(diffMs / 1000 / 60);
  if (diffMins < 1) return "just now";
  if (diffMins < 60) return `${diffMins}m ago`;
  const diffHours = Math.floor(diffMins / 60);
  if (diffHours < 24) return `${diffHours}h ago`;
  const diffDays = Math.floor(diffHours / 24);
  if (diffDays < 30) return `${diffDays}d ago`;
  const diffMonths = Math.floor(diffDays / 30);
  if (diffMonths < 12) return `${diffMonths}mo ago`;
  return `${Math.floor(diffMonths / 12)}y ago`;
}

